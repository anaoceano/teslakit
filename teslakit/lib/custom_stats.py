def Persistences(series):
    'Return series persistences for each element'

    # output dict
    d_pers = {}
    for e in set(series):
        d_pers[e] = []

    #Â analize series
    e0 = None
    while series.any():

        # pol left
        e1 = series[0]
        series = np.delete(series, 0)

        if e1 != e0:
            d_pers[e1].append(1)
        else:
            d_pers[e1][-1]+=1

        # step forward
        e0 = e1

    return d_pers

